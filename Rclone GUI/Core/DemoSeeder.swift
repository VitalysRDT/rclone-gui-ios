    @discardableResult
    private static func makeDemoFiles() -> URL {
        let fm = FileManager.default
        let root = AppGroup.containerURL.appending(path: "DemoFiles", directoryHint: .isDirectory)
        for file in demoTree {
            let url = root.appending(path: file.path)
            let dir = url.deletingLastPathComponent()
            try? fm.createDirectory(at: dir, withIntermediateDirectories: true)
            if !fm.fileExists(atPath: url.path) {
                if file.path.hasSuffix(".txt") || file.path.hasSuffix(".md") {
                    let body = "Rclone GUI — fichier de démonstration.\nCe contenu sert uniquement aux captures App Store.\n"
                    try? Data(body.utf8).write(to: url)
                } else {
                    // Sparse file: realistic logical size, ~zero bytes on disk.
                    fm.createFile(atPath: url.path, contents: nil)
                    if let handle = try? FileHandle(forWritingTo: url) {
                        try? handle.truncate(atOffset: UInt64(max(file.bytes, 0)))
                        try? handle.close()
                    }
                }
            }
            let modDate = Date().addingTimeInterval(-file.daysAgo * 86_400)
            try? fm.setAttributes([.modificationDate: modDate], ofItemAtPath: url.path)
        }
        return root
    }

    // MARK: - SwiftData (Home dashboard liveliness)

    private static func seedSwiftData(_ context: ModelContext) {
        // Idempotent: only seed an empty store.
        let existing = (try? context.fetchCount(FetchDescriptor<SavedLocation>())) ?? 0
        guard existing == 0 else { return }

        // Pinned + recent locations.
        let pinned: [(String, String, String, Int)] = [
            ("iPhone", "Photos", "Photos", 0),
            ("Google Drive", "Projets/2026", "Projets 2026", 1),
        ]
        for (remote, path, name, idx) in pinned {
            context.insert(SavedLocation(
                kind: .pinned, remote: remote, path: path, displayName: name,
                createdAt: Date().addingTimeInterval(-Double(idx + 3) * 86_400),
                lastOpenedAt: Date().addingTimeInterval(-Double(idx + 1) * 3_600),
                openCount: 12 - idx, sortIndex: idx
            ))
        }
        let recent: [(String, String, String, Double)] = [
            ("iPhone", "Vidéos", "Vidéos", 0.4),
            ("Wasabi S3", "archives", "archives", 1.5),
            ("NAS SFTP", "media/films", "films", 6.0),
        ]
        for (remote, path, name, hrs) in recent {
            context.insert(SavedLocation(
                kind: .recent, remote: remote, path: path, displayName: name,
                lastOpenedAt: Date().addingTimeInterval(-hrs * 3_600)
            ))
        }

        // Transfers — two running (drives the "transferts en cours" banner),
        // plus history for the activity metrics.
        let t1 = Transfer(
            kind: .upload, sourceRemote: nil, sourcePath: "Vacances-Bali-2026.MOV",
            destinationRemote: "Backblaze B2", destinationPath: "videos/Vacances-Bali-2026.MOV",
            displayName: "Vacances-Bali-2026.MOV", sourceKind: .photoLibrary,
            bytesTotal: 1_843_200_000, status: .running
        )
        t1.bytesTransferred = 1_186_300_000
        let t2 = Transfer(
            kind: .upload, sourceRemote: nil, sourcePath: "IMG_5012.HEIC",
            destinationRemote: "Photos chiffré", destinationPath: "IMG_5012.HEIC",
            displayName: "IMG_5012.HEIC", sourceKind: .photoLibrary,
            bytesTotal: 3_770_500, status: .running
        )
        t2.bytesTransferred = 2_450_000
        context.insert(t1)
        context.insert(t2)

        let completedNames = [
            "IMG_4821.HEIC", "IMG_4822.HEIC", "Rapport-Q2-2026.pdf",
            "Panorama-Bali.JPG", "Set-live-2026.mp3", "Facture-2026-0142.pdf",
        ]
        for (i, name) in completedNames.enumerated() {
            let t = Transfer(
                kind: .upload, sourcePath: name,
                destinationRemote: "Backblaze B2", destinationPath: "auto/\(name)",
                displayName: name, sourceKind: .photoLibrary,
                bytesTotal: Int64(2_000_000 + i * 900_000), status: .completed
            )
            t.bytesTransferred = t.bytesTotal
            t.finishedAt = Date().addingTimeInterval(-Double(i + 1) * 1_800)
            t.startedAt = Date().addingTimeInterval(-Double(i + 1) * 1_900)
            context.insert(t)
        }
        let failed = Transfer(
            kind: .download, sourceRemote: "Google Drive", sourcePath: "Projets/maquette.sketch",
            destinationPath: "maquette.sketch", displayName: "maquette.sketch",
            sourceKind: .remote, bytesTotal: 142_000_000, status: .failed
        )
        failed.bytesTransferred = 38_000_000
        failed.lastError = "Quota dépassé sur le remote distant"
        context.insert(failed)

        // Photo-sync assets — a mostly-complete backup batch.
        for i in 0..<48 {
            let asset = PhotoSyncAsset(
                localIdentifier: "demo-asset-\(i)",
                mediaType: i % 7 == 0 ? "video" : "image",
                creationDate: Date().addingTimeInterval(-Double(i) * 3_600)
            )
            asset.byteCount = Int64(2_400_000 + (i % 5) * 1_300_000)
            if i < 42 {
                asset.statusRaw = PhotoSyncStatus.completed.rawValue
                asset.completedAt = Date().addingTimeInterval(-Double(i) * 600)
                asset.remotePathsJSON = "[\"Backblaze B2:photos/IMG_\(4000 + i).HEIC\"]"
            } else {
                asset.statusRaw = PhotoSyncStatus.pending.rawValue
            }
            context.insert(asset)
        }

        try? context.save()
    }
