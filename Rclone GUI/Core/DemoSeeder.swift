    /// True when the current launch asked for demo seeding.
    static var isRequested: Bool {
        CommandLine.arguments.contains("--seed-demo")
            || ProcessInfo.processInfo.environment["SEED_DEMO"] == "1"
    }

    /// Entry point — call once, early, before the engine reads the config.
    static func seedIfRequested(container: ModelContainer) async {
        guard isRequested else { return }

        // Skip onboarding so screenshots land on real, in-use surfaces.
        UserDefaults.standard.set(true, forKey: "hasCompletedOnboarding")

        let demoRoot = makeDemoFiles()
        let conf = rcloneConf(localRoot: demoRoot)

        do {
            try await ConfigStore.shared.save(Data(conf.utf8))
            // The engine may not have initialised yet; reload is best-effort.
            try? await RcloneCore.shared.reloadConfigurationFromStore()
        } catch {
            NSLog("[DemoSeeder] config save failed: \(error)")
        }

        seedSwiftData(container.mainContext)

        // Nudge the long-lived tabs to reload from the freshly-seeded config.
        NotificationCenter.default.post(name: .rcloneConfigurationDidChange, object: nil)
    }

