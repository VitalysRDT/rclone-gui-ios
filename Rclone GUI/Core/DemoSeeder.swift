//
//  DemoSeeder.swift
//  Rclone GUI — Core
//
//  Seeds realistic, privacy-safe demo data so the App Store screenshots show
//  the real app *in use* (Guideline 2.3.3) without exposing any real account.
//
//  Activated ONLY when the app is launched with `--seed-demo` (or the
//  SEED_DEMO=1 environment variable). Compiled in DEBUG builds only, so it
//  can never run in a production App Store binary.
//
//  What it sets up:
//    • A plaintext rclone.conf with one *browsable* local remote (`iPhone`,
//      an alias rooted at a seeded demo tree) plus a curated set of cloud
//      remotes (B2, S3/Wasabi, Drive, Dropbox, OneDrive, SFTP) and one
//      `crypt` remote — enough to populate the Files dashboard with colored
//      backend chips and a crypt badge.
//    • A demo file tree (sparse files with realistic names/sizes/dates) so
//      the folder browser and file detail screens show real content.
//    • SwiftData rows (pinned/recent locations, transfers, photo-sync assets)
//      so Home reads as a lively "control center".
//
//  Everything is idempotent and confined to the app's own container.
//

